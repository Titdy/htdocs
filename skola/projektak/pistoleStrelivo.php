        <h2>Druhy Nábojů</h2>
        <ul>
        <li class="font-weight-bold">Roundnose Lead (Kulatá, olovněná špička)</li><br>
        <p>
        Olověná střela s kulatým nosem, zkrácená na LRN, je pevná olověná střela,
         která se zužuje do tupého, zaobleného profilu nosu. Střely LRN jsou po 
         mušketové kouli nejstarším typem střely. Střelci nejčastěji používají 
         náboje LRN pro cvičení na terč s revolvery a puškami.
        </p>
        <li class="font-weight-bold">Full Metal Jacket (Celoplášťová)</li><br>
        <p>
        Střela s celokovovým pláštěm (FMJ) je střela z ručních zbraní sestávající
         z měkkého jádra (často olova) zapouzdřeného ve vnějším plášti ("bunda")
          z tvrdšího kovu, jako je pozlacený kov, měďnatý nikl nebo méně často 
          ocelová slitina. Plášť střely obvykle umožňuje vyšší úsťové rychlosti 
          než holé olovo, aniž by do vývrtu ukládal významné množství kovu. Z
          abraňuje také poškození vývrtů z tvrdé oceli nebo materiálů jádra 
          prorážejících pancíř.
        </p>
        <li class="font-weight-bold">Semi-Wad Cutter</li><br>
        <p>
        Semiwadcutter (SWC) nebo plochý nos je typ víceúčelové střely běžně 
        používané v revolverech. SWC kombinuje vlastnosti tradičních nábojů s 
        kulatým nosem a wadcutter nábojů používaných při střelbě na terč a používá
         se v nábojích do revolverů i pušek pro lov, střelbu na terč a plinking. 
         Plné wadcutters mají často problémy se spolehlivým podáváním ze zásobníků 
         v poloautomatech, takže SWC lze použít, když je požadován skutečný WC, 
         ale nelze je z tohoto důvodu použít.
        </p>
        <li class="font-weight-bold">Hollowpoint</li><br>
        <p>
        Střela s dutým bodem je typ rozšiřující se střely, která se při dopadu 
        rozpíná a způsobuje smrtelnější zásah, aniž by pronikla dále, než je 
        nutné. Střely s dutým hrotem se používají pro kontrolovanou penetraci, 
        kde by přílišná penetrace mohla způsobit vedlejší poškození (například 
        na palubě letadla). Při střelbě na terč se používají pro větší přesnost 
        díky většímu meplatu. Jsou přesnější a předvídatelnější ve srovnání se š
        pičatými střelami, které, přestože mají vyšší balistický koeficient (BC)
        , jsou citlivější na harmonické charakteristiky střely a průhyb větru.
        </p>
        <li class="font-weight-bold">Wad Cutter</li><br>
        <p>
        Wadcutter je speciální kulka s plochým čelem speciálně navržená pro 
        střelbu na papírové cíle, obvykle na blízko a při podzvukových rychlostech
         typicky pod přibližně 900 stop/s (274 m/s). Wadcutters také našli oblibu 
         pro použití v sebeobranných zbraních, jako jsou tupé revolvery ráže 0,38,
          kde jsou v důsledku krátkých délek hlavně maximální rychlosti střely 
          obvykle nízké, typicky pod 900 stop/s (274 m/s), a je žádoucí zlepšená 
          letalita. Wadcutters se často používají v soutěžích v ručních a 
          vzduchovkách.
        </p>
        <img src="img/pageImages/handgun-ammo-types.jpg" class="mx-auto d-block img-fluid">
        </ul>


