    <h2>5 Nejznámnějších kalibrů</h2>
    <p>
        <ul>
            <li class="font-weight-bold">.45 ACP</li><br>
            <p>
            Nic nezpůsobí tolik dramatu jako hodit .45 ACP do jakékoli debaty. U 
            zarputilých a vzteklých fanoušků, kteří přísahají na náboj, není překvapením, 
            že jedním z nejoblíbenějších ráží ručních zbraní je úctyhodný .45 ACP. .45 ACP, 
            vyvinutý v roce 1904 jménem John Moses Browning, již na tomto seznamu, se poprvé 
            objevil vedle Colt's Model 1911. Tyto dvě platformy jsou téměř synonymem pro 
            1911 a .45 ACP, které do sebe zapadají jako arašídové máslo a želé. Střela .45 
            ACP zažila dvě světové války a díky své síle sloužila jako zbraň pro mnoho 
            vojenských a policejních agentur. Velká ráže hladce vklouzne do sebeobrany a 
            skrytého nošení a prokázala, proč je oblíbená mnoha majitelů zbraní. I když ji v
            ětšina zná pro svůj pár z roku 1911, .45 ACP si prorazila cestu i do ručních 
            zbraní s úderníkem a objevila se na platformách jako Glock 21, Sig Sauer 220 a 
            Beretta PX4 Storm.
            </p><br>
            <li class="font-weight-bold">9x19MM</li><br>
            <p>
            Není žádným překvapením, že 9mm náboj dominuje na prvním místě jako 
            nejoblíbenější ráže ručních zbraní. 9mm náboj, vyvinutý na počátku 20. 
            století rakouským konstruktérem Georgem Lugerem, je nejrozšířenějším nábojem 
            v oblasti ručních zbraní. Díky všestrannosti, cenové dostupnosti a rozšířenosti 
            9mm, které lze provozovat spolu s plnohodnotnými ručními zbraněmi, kompaktními 
            pistolemi a dokonce i karabinami ráže pistole, si 9 mm zajistila přední místo v 
            srdcích majitelů zbraní. Náboj, který je obzvláště oblíbený mezi nadšenci pro 
            skryté nošení a sebeobranu, lze nalézt v některých z nejpopulárnějších obranných 
            ručních zbraní, jako jsou Glock 19, Sig Sauer P365, Smith & Wesson Shield a 
            Ruger LC9S.
            </p><br>
            <li class="font-weight-bold">.40 S&W</li><br>
            <p>
            .40 S&W vidělo spojení dvou výrobců zbraní, Smith & Wesson a Winchester. 
            Vyvinutý jako náboj pro vymáhání práva, .40 S&W povstal v důsledku přestřelky 
            FBI v Miami v roce 1986 poté, co byli zabiti dva speciální agenti a pět bylo 
            zraněno. Agentura hledala 10mm náboj do komory v poloautomatické pistoli, 
            kterou požádala Smith & Wesson, aby naplnila. Smith & Wesson se rozhodl 
            navrhnout menší 10mm náboj, který by balisticky odpovídal 10mm, a tak se v roce
             1990 zrodila .40 S&W. .40 S&W zůstává možností pro skryté nosiče a majitele 
             ručních zbraní osobní obrany, kteří hledají náboj nabitý silou ve vlastních 
             M&P, H&K VP40 a Sig Sauer P320 společnosti Smith & Wesson.
            </p><br>
            <li class="font-weight-bold">.38 SPECIAL</li><br>
            <p>
            .38 Special pochází s laskavým svolením výrobce střelných zbraní Smith & Wesson.
             .38 SPL, vyvinutá v roce 1899 jako upgrade na .38 Long Colt, se stala synonymem
              pro policejní práci. .38 SPL, která sloužila jako standardní služební pistolový
               náboj pro orgány činné v trestním řízení v USA, loajálně sloužila vedle 
               policistů od 20. do 90. let. .38 SPL je nejlépe známý tím, že nabízí 
               zvládnutelný zpětný ráz spojený s přesností v různých střeleckých d
               isciplínách, včetně střelby na terč, soutěže, lovu a sebeobrany. .38 
               Special lze nalézt na zbraních jako Smith & Wesson 642 a Ruger LCR.
            </p><br>
            <li class="font-weight-bold">.380 ACP</li><br>
            <p>
            Náboj .380 ACP, potomek Johna Mosese Browninga, je od svého debutu na 
            počátku 20. století oblíbeným nábojem pro sebeobranu. Náboj .380 Automatic 
            Colt Pistol, neboli ACP, představený společností Colt v roce 1908 ve své 
            bezkladivkové pistoli Colt Model 1908, poskytoval méně zpětného rázu, díky 
            čemuž byla střela snadno zvládnutelná. Ideální pro nošení kapesní pistole 
            jako u Ruger LCP nebo Sig Sauer P238, .380 ACP zůstává jedním z nejoblíbenějších 
            nábojů pro ty, kteří hledají menší kombo pistole/kulaté jako záložní zbraň nebo 
            možnost skrytého nošení.
