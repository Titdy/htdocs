    <h2>Speedloader</h2>
    <p>
    Naplnění zásobníku střelné zbraně, zejména takového, který má velkou kapacitu a
    odpovídající vysoký tlak pružiny tlačí náboje k horní části zásobníku, může být poměrně 
    obtížné. Pro usnadnění tohoto úkolu je k dispozici řada zařízení, která se někdy nazývají
     rychlozavaděče, ale jsou běžněji známá jako nakladače zásobníků, stahovací spony, 
     lžíce nebo vodítka stahovacích klipů. Nejjednodušší jsou nenákladná zařízení, která s
     tlačují horní náboj v zásobníku, což umožňuje částečné vložení dalšího náboje bez 
     tlaku na něj. Nazývají se také „spořiče palce“ a řeší spíše snadnost načítání než 
     rychlost načítání. K dispozici jsou také zařízení pro některé populární střelné zbraně,
      jako je Ruger 10/22, které přijímají volné střelivo a nabijí náboj do zásobníku 
      jednoduchým stisknutím tlačítka nebo otočením kliky.
      
